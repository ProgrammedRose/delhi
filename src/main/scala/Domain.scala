// =============================================================================
// Domain.scala — предметные типы торгового автомата
// =============================================================================
// Содержит только типы и конфигурацию по умолчанию.
// Никакой логики — только данные.
//
// VendingReader, VendingWriter, VendingMachine из ЛР1 удалены:
// их роль теперь выполняют интерпретаторы в Interpreters.scala.
// =============================================================================


// -----------------------------------------------------------------------------
// Правило скидки: функция (товар, час) => коэффициент цены.
// 1.0 = без скидки, 0.9 = -10% и т.д.
// -----------------------------------------------------------------------------
type DiscountRule = (String, Int) => Double


// -----------------------------------------------------------------------------
// Конфигурация автомата — неизменяемая, создаётся один раз при старте.
// -----------------------------------------------------------------------------
final case class VendingConfig(
                                prices:        Map[String, Int],  // название → цена в копейках
                                acceptedCoins: Set[Int],          // допустимые номиналы монет
                                maxInsertable: Int,               // максимум внесённой суммы за раз
                                discountRule:  DiscountRule       // правило скидки
                              )


// -----------------------------------------------------------------------------
// Вспомогательные типы результатов.
// Result[A] = Either[String, A]: Right — успех, Left — сообщение об ошибке.
// Change     — список монет сдачи в копейках.
// -----------------------------------------------------------------------------
type Result[A] = Either[String, A]
type Change    = List[Int]


// -----------------------------------------------------------------------------
// Состояние автомата — полный снимок в конкретный момент времени.
// «S» в State[MachineState, A].
// -----------------------------------------------------------------------------
final case class MachineState(
                               stock:    Map[String, Int],  // остатки товаров: название → количество
                               inserted: Int,               // внесённая сумма (до завершения транзакции)
                               revenue:  Int,               // накопленная выручка
                               cashBox:  Map[Int, Int]      // касса: номинал → количество монет
                             )

object MachineState:
  val initial: MachineState = MachineState(
    stock   = Map("cola" -> 5, "water" -> 5, "juice" -> 5, "coffee" -> 5),
    inserted = 0,
    revenue  = 0,
    cashBox  = Map(10 -> 20, 50 -> 10, 100 -> 10, 200 -> 5, 500 -> 5)
  )


// -----------------------------------------------------------------------------
// Конфигурация по умолчанию — используется в Main.
// -----------------------------------------------------------------------------
object DefaultConfig:

  val discountRule: DiscountRule = (product, hour) =>
    if product == "juice" && hour >= 18 then 0.9 else 1.0

  val config: VendingConfig = VendingConfig(
    prices        = Map("cola" -> 150, "water" -> 80, "juice" -> 120, "coffee" -> 200),
    acceptedCoins = Set(10, 50, 100, 200, 500, 1000, 2000, 5000),
    maxInsertable = 10000,
    discountRule  = discountRule
  )